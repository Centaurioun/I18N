CREATE TABLE Types
(
  Id INTEGER NOT NULL,
  Name NVARCHAR(50) NOT NULL,
  Description NVARCHAR(100) NOT NULL,
  Expression NVARCHAR(50) NOT NULL,
  Price FLOAT NOT NULL,
  Count INTEGER NOT NULL,
  Active BIT NOT NULL,
  ImageRaw IMAGE NOT NULL,
  ImageBase64 NTEXT NOT NULL,
  XmlRaw IMAGE NOT NULL,
  XmlBase64 NTEXT NOT NULL,
  XmlText NTEXT NOT NULL,
  JsonRaw IMAGE NOT NULL,
  JsonBase64 NTEXT NOT NULL,
  JsonText NTEXT NOT NULL,
  HtmlRaw IMAGE NOT NULL,
  HtmlBase64 NTEXT NOT NULL,
  HtmlText NTEXT NOT NULL,
  PRIMARY KEY(Id)
)