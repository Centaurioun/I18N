CREATE TABLE Types
(
  Id INTEGER NOT NULL,
  Name VARCHAR(50) NOT NULL,
  Description VARCHAR(100) NOT NULL,
  Expression VARCHAR(50) NOT NULL,
  Price FLOAT NOT NULL,
  ProductCount INTEGER NOT NULL,
  Active YESNO NOT NULL,
  ImageRaw IMAGE NOT NULL,
  ImageBase64 MEMO NOT NULL,
  XmlRaw IMAGE NOT NULL,
  XmlBase64 MEMO NOT NULL,
  XmlText MEMO NOT NULL,
  JsonRaw IMAGE NOT NULL,
  JsonBase64 MEMO NOT NULL,
  JsonText MEMO NOT NULL,
  HtmlRaw IMAGE NOT NULL,
  HtmlBase64 MEMO NOT NULL,
  HtmlText MEMO NOT NULL,
  PRIMARY KEY(Id)
)