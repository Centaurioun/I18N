CREATE TABLE Types
(
  Id INTEGER NOT NULL,
  Name VARCHAR(50) NOT NULL,
  Description VARCHAR(100) NOT NULL,
  Expression VARCHAR(50) NOT NULL,
  Price FLOAT NOT NULL,
  ProductCount INTEGER NOT NULL,
  Active BOOLEAN NOT NULL,
  ImageRaw BLOB NOT NULL,
  ImageBase64 TEXT NOT NULL,
  XmlRaw BLOB NOT NULL,
  XmlBase64 TEXT NOT NULL,
  XmlText TEXT NOT NULL,
  JsonRaw BLOB NOT NULL,
  JsonBase64 TEXT NOT NULL,
  JsonText TEXT NOT NULL,
  HtmlRaw BLOB NOT NULL,
  HtmlBase64 TEXT NOT NULL,
  HtmlText TEXT NOT NULL,
  PRIMARY KEY(Id)
)